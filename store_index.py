from pinecone import Pinecone, ServerlessSpec

api_key = os.getenv("PINECONE_API_KEY")
if not api_key:
    raise Exception("API key missing. Set PINECONE_API_KEY environment variable.")

pc = Pinecone(api_key=api_key)

index_name = "medicalbot"

if index_name not in pc.list_indexes().names():
    pc.create_index(
        name=index_name,
        dimension=384,
        metric="cosine",
        spec=ServerlessSpec(
            cloud="aws",
            region="us-east-1"
        )
    )

print("Index created or exists already.")


